package com.pret.lalala.pret.Model;

public class User {

    public String nama;
    public String email;
    public String uname;
    public String id;

    public User() {

    }

    public User(String nama, String email, String uname, String id) {

        this.nama = nama;
        this.email = email;
        this.uname = uname;
        this.id = id;
    }
}
